import numpy as np
# s+2p+d = 5
# 2s-p-3d = 7
# 2s-f-2g=4
# 2p-2f+g =2
# 2d -f +3g = 0
A = np.array([[1,2,1,0,0],[2,-1,-3,0,0],[2,0,0,-1,-2],[0,2,0,-2,1],[0,0,2,-1,3]])
b = np.array([5,7,4,2,0])
x = np.linalg.solve(A,b)
# x11 = s, x21 = p, x31 = d ,x41 = f, x51 = g
print('x',x)
# s = 3.066666...
# p = 1.3333333..
# d = -0.733333...
# f = 0.6933333...
# g = 0.72